public class kilo_to_pound
{
public static void main(String [] agrs)
	{
	final double  LB = 0.454 ;
	double x = 10 / LB ;
	double y = 50 / LB ;
	double z = 100 / LB ;

	System.out.println("10 kilogram =" + x + "lb");
	System.out.println("50 kilogram =" + y + "lb");
	System.out.println("100 kilogram =" + z + "lb");
	}
}