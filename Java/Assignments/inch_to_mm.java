public class inch_to_mm
{
public static void main(String [] agrs)
	{
	final double  INCH = 25.4 ;
	double x = 2 * INCH ;
	double y = 5 * INCH ;
	double z = 100 * INCH ;

	System.out.println("2 inch =" + x + "mm");
	System.out.println("5 inch =" + y + "mm");
	System.out.println("100 inch =" + z + "mm");
	}
}